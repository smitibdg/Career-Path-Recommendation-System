const Profile = require('../models/Profile');
const User = require('../models/User');

// Create or update user profile
exports.createOrUpdateProfile = async (req, res) => {
  try {
    const userId = req.user.userId;
    const profileData = req.body;

    console.log('üî¥ BACKEND: Profile creation/update request');
    console.log('üî¥ User ID:', userId);
    console.log('üî¥ Profile Data:', JSON.stringify(profileData, null, 2));

    // Check if profile already exists
    let profile = await Profile.findOne({ user: userId });

    if (profile) {
      console.log('üìù Updating existing profile...');
      // Update existing profile
      Object.assign(profile, profileData);
      profile.profileCompleted = true;
      profile.updatedAt = new Date();
      await profile.save();
    } else {
      console.log('üìù Creating new profile...');
      // Create new profile
      profile = new Profile({
        user: userId,
        ...profileData,
        profileCompleted: true
      });
      await profile.save();
    }

    // Update user's profileCompleted status
    await User.findByIdAndUpdate(userId, { 
      profileCompleted: true,
      updatedAt: new Date()
    });

    // Populate user data for response
    await profile.populate('user', 'name email');

    console.log('‚úÖ Profile saved successfully');
    console.log('‚úÖ Profile ID:', profile._id);
    console.log('‚úÖ Education Level:', profile.educationLevel);
    console.log('‚úÖ Assessment Level:', profile.assessmentLevel);

    // CONSISTENT RESPONSE FORMAT
    const response = {
      success: true,
      message: 'Profile saved successfully',
      profile: profile.toObject ? profile.toObject() : profile
    };

    console.log('üì§ Sending response format check:');
    console.log('üì§ - success:', response.success);
    console.log('üì§ - profile exists:', !!response.profile);
    console.log('üì§ - profile id:', response.profile._id);

    res.status(200).json(response);

  } catch (error) {
    console.error('‚ùå Profile save error:', error);
    console.error('‚ùå Error stack:', error.stack);
    
    res.status(500).json({
      success: false,
      message: 'Server error saving profile',
      error: error.message
    });
  }
};

// Get user profile
exports.getProfile = async (req, res) => {
  try {
    const userId = req.user.userId;
    
    console.log('üìñ GET Profile - User ID:', userId);
    
    const profile = await Profile.findOne({ user: userId }).populate('user', 'name email');
    
    if (!profile) {
      console.log('‚ùå No profile found for user:', userId);
      return res.status(404).json({
        success: false,
        message: 'Profile not found'
      });
    }

    console.log('‚úÖ Profile found and returned');
    console.log('‚úÖ Profile details:', {
      id: profile._id,
      name: profile.name,
      educationLevel: profile.educationLevel,
      assessmentLevel: profile.assessmentLevel
    });
    
    res.status(200).json({
      success: true,
      message: 'Profile retrieved successfully',
      profile: profile.toObject ? profile.toObject() : profile
    });

  } catch (error) {
    console.error('‚ùå Get profile error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error fetching profile',
      error: error.message
    });
  }
};

// Update profile
exports.updateProfile = async (req, res) => {
  try {
    const userId = req.user.userId;
    const updateData = req.body;

    console.log('üîÑ UPDATE Profile request');
    console.log('üîÑ User ID:', userId);
    console.log('üîÑ Update Data:', updateData);

    const profile = await Profile.findOneAndUpdate(
      { user: userId },
      { ...updateData, updatedAt: new Date() },
      { new: true, runValidators: true }
    ).populate('user', 'name email');

    if (!profile) {
      return res.status(404).json({
        success: false,
        message: 'Profile not found'
      });
    }

    console.log('‚úÖ Profile updated successfully');

    res.status(200).json({
      success: true,
      message: 'Profile updated successfully',
      profile: profile.toObject ? profile.toObject() : profile
    });

  } catch (error) {
    console.error('‚ùå Profile update error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error updating profile',
      error: error.message
    });
  }
};

// Complete assessment
exports.completeAssessment = async (req, res) => {
  try {
    const userId = req.user.userId;
    const { testType, responses, score } = req.body;

    console.log('üèÜ Complete Assessment - User ID:', userId, 'Test:', testType);

    const profile = await Profile.findOne({ user: userId });
    
    if (!profile) {
      return res.status(404).json({
        success: false,
        message: 'Profile not found'
      });
    }

    // Update assessment completion (only if profile has this method)
    if (profile.assessmentsCompleted && typeof profile.assessmentsCompleted.set === 'function') {
      profile.assessmentsCompleted.set(testType, {
        completed: true,
        completedAt: new Date(),
        score: score,
        responses: responses
      });
    }

    await profile.save();

    console.log('‚úÖ Assessment completed successfully');

    res.status(200).json({
      success: true,
      message: 'Assessment completed successfully',
      assessmentProgress: profile.getAssessmentProgress ? profile.getAssessmentProgress() : {}
    });

  } catch (error) {
    console.error('‚ùå Complete assessment error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error completing assessment',
      error: error.message
    });
  }
};

